using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CaboTester
{
    class CaboTester
    {
        static Random rnd = new Random();

        static int[] winsPerSum = new int[27];
        static int[] losesPerSum = new int[27];
        static void Main(string[] args)
        {
            for(int i = 0; i < 10000000; i++) {
                CaboGame game = new CaboGame(3);

                var result = game.SimulateQuickCabo();
                //Console.WriteLine($"{result.Item1}, {result.Item2}");
                //Console.WriteLine($"{result.Item1}, " + (result.Item2 == 0 ? "1" : "0"));
                
                if(result.Item2 == 0) {
                    winsPerSum[result.Item1]++;
                } else {
                    losesPerSum[result.Item1]++;
                }
            }
            
            for(int i = 0; i < 27; i++) {
                Console.WriteLine($"{i}:   {(double)winsPerSum[i] / ((double)winsPerSum[i] + losesPerSum[i])}");
            }


            Console.ReadLine();
        }
    }
}

